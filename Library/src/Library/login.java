    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        String name = txtName.getText();
        String pass = (txtPassword.getText()); 

        // ตรวจสอบว่ากรอกข้อมูลครบหรือไม่
        if (name.isEmpty() || pass.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pls Enteer name and password");
            return;
        }

        try {
            
            con=DBConnect.getConnect();

            // 2. เขียน SQL Query ตรวจสอบ S_Name และ password
            String sql = "SELECT * FROM staff WHERE S_Name = ? AND password = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, name);
            pst.setString(2, pass);

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                
                javax.swing.JOptionPane.showMessageDialog(this, "succeed!");

                
                this.dispose(); 

                
                Main_frm main = new Main_frm();
                main.setVisible(true);
                main.setLocationRelativeTo(null); 

            } else {
                
                javax.swing.JOptionPane.showMessageDialog(this, "The info is inconsistent.");
                txtPassword.setText(""); 
                txtName.requestFocus();  
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "An error occurred in the database connection.: " + e.getMessage());
        }
    }//GEN-LAST:event_btnloginActionPerformed
